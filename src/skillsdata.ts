let PARAMTERIZATION: RegExp = /{{([^}]+)}}/;
var expandParameters = function (input: string) {
  if (!PARAMTERIZATION.test(input)) {
    console.log("Didn't see any parameters...");
    return [input];
  }
  let groups = input.match(PARAMTERIZATION);
  var v = groups[1].trim();
  // console.log("matched "+groups[0]+" with variable "+v);
  var replacements = SKILL_PARAMETERS[v];
  if (!replacements) {
    console.log('COULD NOT FIND EXPANSION FOR VARIABLE ' + v);
    return [input];
  }
  // carve out the part being replaced that looks like {{ }} so we can
  // put the string back together
  let startOfReplacement = input.indexOf(groups[0]);
  let endOfReplacement = startOfReplacement + groups[0].length;
  var front = input.substring(0, startOfReplacement);
  var end = input.substring(endOfReplacement, input.length);
  //console.log("Would put replacements between \""+front+"\" and \""+end+"\"");
  var output = [];

  for (let r of replacements) {
    var sub = {};
    sub[v] = r;
    output.push({ val: front + r + end, sub: sub });
  }

  return output;
};

var replaceParameters = function (input, env, silent) {
  if (!PARAMTERIZATION.test(input)) {
    // console.log("Didn't see any parameters...");
    return input;
  }
  let groups = input.match(PARAMTERIZATION);
  var v = groups[1].trim();
  // console.log("matched "+groups[0]+" with variable "+v);
  var replacement = env[v];
  if (!replacement) {
    if (!silent) {
      console.log('COULD NOT FIND VALUE FOR VARIABLE ' + v);
    }
    return input;
  }
  // carve out the part being replaced that looks like {{ }} so we can
  // put the string back together
  let startOfReplacement = input.indexOf(groups[0]);
  let endOfReplacement = startOfReplacement + groups[0].length;
  var front = input.substring(0, startOfReplacement);
  var end = input.substring(endOfReplacement, input.length);
  //console.log("Would put replacements between \""+front+"\" and \""+end+"\"");
  return front + replacement + end;
};

export class SkillNode {
  skill;
  children: SkillNode[];
  parent: SkillNode;
  parameters;
  // if this has been parameterized, store that specific key for this
  parameterizedKey;
  // if this is really just a placeholder node, also mark that
  isMissing;

  constructor(skill) {
    this.skill = skill;
    this.children = [];
    this.parameters = {};
    this.isMissing = false;
    this.parameterizedKey;
    this.parent = null;
  }

  markMissing() {
    this.isMissing = true;
  }

  addChild(node) {
    this.children.push(node);
  }

  addParent(node) {
    this.parent = node;
  }

  getParent() {
    return this.parent;
  }

  addParameter(k, v) {
    this.parameters[k] = v;
  }

  isParamaterized() {
    return this.skill['PARAMETERIZED'];
  }

  getName() {
    return this.skill['Skill Name'];
  }

  getParameterizedName() {
    if (this.skill['PARAM_NAME']) {
      return replaceParameters(this.skill['PARAM_NAME'], this.parameters, true);
    }
    return this.getName();
  }

  getBase() {
    return this.skill['Base'];
  }
}

let allNodes = {};
// these will hold placeholders for parameterization-generated nodes.  It maps
// their concrete type to a set of parameterized nodes
let paramNodes = {};
// make node objects for all of these
for (let key in skillLookup) {
  var skill = skillLookup[key];
  // if we're already done, we're already done
  if (allNodes[key]) {
    continue;
  }

  // handle aliases in skillLookup
  var trueKey = skill['Skill Name'];
  var isAlias = false;
  if (trueKey != key) {
    isAlias = true;
  }
  // the real skill could be in there and we could need to add another pointer to it
  if (isAlias && allNodes[trueKey]) {
    allNodes[key] = allNodes[trueKey];
    continue;
  }

  let n = new SkillNode(skill);

  if (skillLookup[key]['PARAMETERIZED']) {
    // the case when a thing is parameterized
    // check the title of this skill to see if it needs parameterization.
    // If so, we generate a series of nodes for it
    // console.log("Would parameterize: "+key);

    if (skill['PARAM_NAME']) {
      // console.log("Skill "+key + " has a name to parameterize: "+skill["PARAM_NAME"]);
      var expandedNames = expandParameters(skill['PARAM_NAME']);
      var paramSet = {};
      var allOptions = [];

      for (let newSkillData of expandedNames) {
        // console.log("Would generate skill "+newSkillData.val);
        let param_node = new SkillNode(skill);
        // console.log(newSkillData.sub);
        for (let p in newSkillData.sub) {
          // console.log("Adding parameter "+p+ " = "+newSkillData.sub[p]);
          param_node.addParameter(p, newSkillData.sub[p]);
        }

        // and if it has manual paramaters specified, this is when we'll add them
        if (skill['PARAM_SUB']) {
          var params = skill['PARAM_SUB'];
          for (let p in params) {
            // console.log("Adding parameter "+p+ " = "+newSkillData.sub[p]);
            param_node.addParameter(p, params[p]);
          }
        }
        param_node.parameterizedKey = newSkillData.val;

        allNodes[newSkillData.val] = param_node;
        allOptions.push(param_node);
        // still necessary?
        // console.log("Adding "+param_node.getName()+" to a param set under name "+newSkillData.val);
        paramSet[newSkillData.val] = param_node;
      }
      paramSet['ALL_NODES_ARRAY'] = allOptions;
      // we ALSO need to associate the true key with all of these expanded nodes
      // This may not be necessary?
      // console.log("Mapping a paramset to "+trueKey);
      paramNodes[trueKey] = paramSet;
    } else {
      // if its title isn't the part that required parameterization, just add it like normal for now
      // The parameterized part must be its children, and they'll be processed later

      // but if it has manual paramaters specified, this is when we'll add them
      if (skill['PARAM_SUB']) {
        var params = skill['PARAM_SUB'];
        for (let p in params) {
          // console.log("Adding parameter "+p+ " = "+newSkillData.sub[p]);
          n.addParameter(p, params[p]);
        }
      }

      allNodes[key] = n;
    }
  } else {
    // the simple and thankfully dominant case
    // let n = new SkillNode(skillLookup[key]);
    allNodes[key] = n;
  }

  // by here, it has been added under this key.  It might not be the real one.  Link them up
  if (isAlias) {
    allNodes[trueKey] = allNodes[key];
  }
}

var getNode = function (key, params, calledFrom) {
  // if you can find it directly, just return it
  if (allNodes[key]) {
    return allNodes[key];
  }

  // we haven't found it.  Is it paramaterized?
  // Seek the true key
  var skill = skillLookup[key];
  if (!skill) {
    // console.log("COULD NOT FIND a skill for "+key);
    var emptyNode = new SkillNode({ 'Skill Name': key });
    emptyNode.markMissing();
    return emptyNode;
  }

  var trueKey = skill['Skill Name'];
  if (paramNodes[trueKey]) {
    // replace the string
    let finalKey = replaceParameters(
      skill['PARAM_NAME'],
      params,
      calledFrom == 'ROOT'
    );
    // console.log("--  Looking up parammeterized key "+finalKey+" called from "+calledFrom);
    return paramNodes[trueKey][finalKey];
  }
  console.log("ERROR IN getNode(): Couldn't find node for key " + key);
  return null;
};

// another FINAL pass to build tree structure
let finalForest: SkillNode[] = [];
let visited = {};
function processNode(n: SkillNode) {
  if (!n) {
    console.log('CALLING PROCESS NODE WITH BAD NODE');
    return;
  }
  // this might just be a placeholder for missing node
  if (n.isMissing) {
    return n;
  }
  // console.log("Possibly processing node n: "+n.getName());
  var trueKey = n.getName();
  // there is a fork in the logic. For nodes with parameterized keys, we use those
  var keyToCheck = trueKey;
  if (n.isParamaterized()) {
    if (n.parameterizedKey) {
      keyToCheck = n.parameterizedKey;
    }
  }

  if (visited[keyToCheck]) {
    return n;
  }
  visited[keyToCheck] = true;
  //console.log("Processing key "+trueKey+ " ("+SkillNameTable[trueKey]+")");
  let verbose = false;
  // if (n.getName() == "COMBAT_INITIATIVE_BONUS") {
  //   verbose = true;
  // }
  // process up
  let parentKey = n.skill['Parent Skill'];
  if (parentKey) {
    if (n.skill['PARAM_PARENT']) {
      parentKey = replaceParameters(
        n.skill['PARAM_PARENT'],
        n.parameters,
        false
      );
    }

    // if (parentKey.startsWith("COMBAT_INITIATIVE_BONUS")) {
    //   console.log("node "+n.getName()+" looking up parent key "+parentKey);
    // }
    // console.log("\tLooking up parent with key = "+parentKey);
    // You never know, the parent might need interpolation
    var pNode: SkillNode = getNode(parentKey, n.parameters, n.getName());

    // console.log("-Got node for "+parentKey);
    processNode(pNode);
  } else {
    // if we're the top, add ourselves to the forest structure
    finalForest.push(n);
    // console.log("\tAdding root skill "+SkillNameTable[n.getName()]);
    // console.log("\tAdding root skill "+n.getName());
  }

  // process down. This is where we add our children
  if (n.skill['Sub Skills']) {
    for (let child of n.skill['Sub Skills']) {
      if (verbose) {
        console.log(n.getName() + ' looking up child ' + child);
      }
      var childNode: SkillNode = getNode(child, n.parameters, n.getName());
      if (!childNode) {
        console.log(
          'Skipping child ' + child + " as it doesn't seem to exist..."
        );
        continue;
      }
      if (verbose) {
        console.log(n.getName() + ' processing then adding childnode ');
        console.log(childNode);
      }
      // console.log("Processing child "+childNode.getName());
      processNode(childNode);

      if (childNode.isMissing) {
        // Assume children are in our category and, for the sake of display,
        // pass our caetgory down to empty nodes
        childNode.skill['Skill Type'] = n.skill['Skill Type'];
      }
      // link up and down
      n.addChild(childNode);
      childNode.addParent(n);
    }
  }
}

for (let key in skillLookup) {
  if (visited[key]) {
    continue;
  }
  var n = getNode(key, {}, 'ROOT');
  if (n) {
    processNode(n);
  }
}

